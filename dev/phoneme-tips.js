// 取得發音建議的函式
function getPhonemeTip(azurePhoneme) {
  // 1. 確保轉成小寫
  const ph = azurePhoneme.toLowerCase();
  
  // 2. 先嘗試直接在字典裡找 (如果傳進來的是我們已經轉譯好的 local 標籤)
  if (PhonemeDictionary[ph]) return PhonemeDictionary[ph];

  // 3. 如果傳進來的是 Azure 原生的標籤，嘗試透過 AZURE_TO_LOCAL_MAP 轉換
  // 注意：需要確保 phonics-config.js 有被載入
  if (typeof AZURE_TO_LOCAL_MAP !== 'undefined') {
    const localKey = AZURE_TO_LOCAL_MAP[ph];
    if (localKey && PhonemeDictionary[localKey]) {
      return PhonemeDictionary[localKey];
    }
  }

  // 4. 如果都找不到，給個預設值
  return "請提醒孩子放慢語速，聽聽標準發音後再試一次喔！";
}